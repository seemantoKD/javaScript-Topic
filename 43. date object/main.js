// default date without customize
var date = new Date();
console.log(date);

// only for showing year
var year = date.getFullYear();
console.log(year);

// only for showing month
var month = date.getMonth();
console.log(month); // 0 means january

// only for showing currentDate
var currentDate = date.getDate();
console.log(currentDate);

// only for showing currentDay
var currentDay = date.getDay();
console.log(currentDay); // 0 means saturday

// only for showing current hours
var h = date.getHours();
console.log(h); // 0 means rat 12 ta

// only for showing current minutes
var m = date.getMinutes();
console.log(m);